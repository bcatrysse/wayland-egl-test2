/* ---------------- Runtime configuration ---------------- */
static volatile sig_atomic_t g_running = 1;
static void handle_sigint(int) { g_running = 0; }

static int32_t g_init_width  = 1280;
static int32_t g_init_height = 720;
static int     g_swap_interval = 1;

/* ---------------- Application state ---------------- */
struct app {
    /* Wayland core */
    struct wl_display     *wl_display;
    struct wl_registry    *wl_registry;
    struct wl_compositor  *wl_compositor;

    /* SimpleShell (Westeros) */
    struct wl_simple_shell *wl_simple_shell;   /* global */
    uint32_t                ss_surface_id;     /* surfaceId received via surface_id event */
    bool                    ss_active;         /* we’re running in SimpleShell mode once surface_id arrives */

    /* xdg-shell (fallback) */
    struct xdg_wm_base    *xdg_wm_base;
    struct wl_surface     *wl_surface;
    struct xdg_surface    *xdg_surface;
    struct xdg_toplevel   *xdg_toplevel;

    /* EGL */
    EGLDisplay  egl_display;
    EGLConfig   egl_config;
    EGLContext  egl_context;
    EGLSurface  egl_surface;
    struct wl_egl_window *egl_window;

    /* state */
    int have_configure;              /* xdg_surface first configure received */
    int width, height;               /* current size */
    int pending_width, pending_height; /* xdg_toplevel.configure suggested size */

    /* mode */
    bool prefer_simpleshell;         /* set when wl_simple_shell global is present */
};

/* ---------------- Helpers ---------------- */
static void die(const char *msg) {
    fprintf(stderr, "FATAL: %s\n", msg);
    exit(EXIT_FAILURE);
}
static void die_egl(const char *where) {
    EGLint err = eglGetError();
    fprintf(stderr, "FATAL EGL (%s): 0x%04x\n", where, err);
    exit(EXIT_FAILURE);
}
static void load_env_overrides(void) {
    const char *w = getenv("WIDTH");
    const char *h = getenv("HEIGHT");
    const char *si = getenv("SWAP_INTERVAL");
    if (w)  g_init_width = atoi(w);
    if (h)  g_init_height = atoi(h);
    if (si) g_swap_interval = atoi(si);
}

