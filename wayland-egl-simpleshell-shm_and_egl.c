/*
 * Wayland + EGL sample that:
 *  - Prefers Westeros wl_simple_shell when available (listen to surface_id, set_name)
 *  - Falls back to xdg_wm_base on non-Westeros compositors
 *
 * Build notes :
 
 wayland-scanner client-header \
  < /usr/share/wayland-protocols/stable/xdg-shell/xdg-shell.xml \
  > xdg-shell-client-protocol.h

wayland-scanner private-code \
  < /usr/share/wayland-protocols/stable/xdg-shell/xdg-shell.xml \
  > xdg-shell-protocol.c

wayland-scanner client-header \
  < ./simpleshell.xml \
  > simpleshell-client-protocol.h

wayland-scanner private-code \
  < ./simpleshell.xml \
  > simpleshell-protocol.c

gcc -Wall -Wextra -O0 -g -o wayland-egl-simpleshell-test \
  wayland-egl-simpleshell.c \
  xdg-shell-protocol.c \
  simpleshell-protocol.c \
  $(pkg-config --cflags --libs wayland-client wayland-egl egl glesv2) \
  -lm
 *
 */

